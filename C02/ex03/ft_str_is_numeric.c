#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	x;
	int	i;

	x = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 48 && str[i] <= 57)
		{
			x = 0;
			i++;
		}
	}
	return (x);
}
